    private void cmbTypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbTypeItemStateChanged
        try {
            List<LoginDTO> all = ManageLoginController.getAll();
            cmbLoginID.removeAllItems();
            if (all != null&&cmbType.getSelectedIndex()!=-1) {
                for (LoginDTO loginDTO : all) {
                    if ((loginDTO.getLoginType()).equals(cmbType.getSelectedItem().toString())) {
                        cmbLoginID.addItem(loginDTO.getLoginID());
                    }
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(lk.ijse.wijithafc.view.panels.UserSettingPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbTypeItemStateChanged

