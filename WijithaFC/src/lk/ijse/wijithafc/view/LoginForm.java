    private void loadTypes() {
        cmbType.removeAllItems();;
        cmbType.addItem("ADMIN");
        cmbType.addItem("USER");
    }
    private void logIN(){
        try {
            LoginDTO searchLogin = ManageLoginController.searchLogin(cmbLoginID.getSelectedItem().toString());
            if (searchLogin.getPassword().equals(new String(txtPassword.getPassword()))) {
                MainFrame mainFrame = new lk.ijse.wijithaFC.view.MainFrame(searchLogin.getLoginID(),searchLogin.getLoginType());
                mainFrame.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Invalid password");
                txtPassword.setText(null);
            }
        } catch (Exception ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
