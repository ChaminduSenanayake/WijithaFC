    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        try {
            LoginDTO searchLogin = ManageLoginController.searchLogin(cmbLoginID.getSelectedItem().toString());
            if (searchLogin.getPassword().equals(new String(txtPassword.getPassword()))) {
                log=true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Invalid password");
                txtPassword.setText(null);
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAdminLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginMouseClicked

