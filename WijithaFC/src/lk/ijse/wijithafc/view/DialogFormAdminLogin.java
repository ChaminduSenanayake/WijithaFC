    private void loadAll() {
        cmbLoginID.removeAllItems();
        try {
            List<LoginDTO> all = ManageLoginController.getAll();
            if (all != null) {
                for (LoginDTO loginDTO : all) {
                    if (loginDTO.getLoginType().equals("ADMIN")) {
                        cmbLoginID.addItem(loginDTO.getLoginID());
                    }
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAdminLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

