    private void btnAdaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAdaMouseClicked
        try {

            ItemDTO dTO = new ItemDTO(txtCode.getText(), txtdesc.getText(), cmbCategory.getSelectedItem().toString(), Double.parseDouble(txtUnitprice.getText()), Integer.parseInt(txtQtyOnHand.getText()), Integer.parseInt(txtDiscountQty.getText()), Double.parseDouble(txtDiscount.getText()));
            boolean addItem = ManageItemsController.addItem(dTO);
            if (addItem) {
                JOptionPane.showMessageDialog(null, "Item has been added successfully");
                this.dispose();
                ManageItemsController.releaseItem(txtCode.getText());
            } else {
                JOptionPane.showMessageDialog(null, "Failed");
            }

        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAdaMouseClicked

