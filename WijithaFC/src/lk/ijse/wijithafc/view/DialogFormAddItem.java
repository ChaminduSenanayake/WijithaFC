    private void loadNewID() {
        try {
            String lastID = IDGenerator.getLastID(IDGenerator.IdType.ITEM);
            txtCode.setText(lastID);
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    @Override
    public void updateObservers() throws Exception {
        loadNewID();
    }

    private void loadCat() {
        cmbCategory.removeAllItems();
        try {
            List<CategoryDTO> all = CategoryController.getAll();
            if (all != null) {
                for (CategoryDTO categoryDTO : all) {
                    cmbCategory.addItem(categoryDTO.getCategory());
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void addCat() {
        CategoryDTO categoryDTO = new CategoryDTO();
        categoryDTO.setCategory(txtCategory.getText());
        try {
            boolean addCat = CategoryController.addCat(categoryDTO);
            if (addCat) {
                JOptionPane.showMessageDialog(rootPane, "Added");
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddItem.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
