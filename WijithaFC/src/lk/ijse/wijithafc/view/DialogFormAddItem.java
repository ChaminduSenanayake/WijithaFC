    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        try {
            List<CategoryDTO> all = CategoryController.getAll();
            if (all != null) {
                for (CategoryDTO categoryDTO : all) {
                    if (categoryDTO.getCategory().equals(txtCategory.getText())) {
                        boolean deleteCat = CategoryController.deleteCat(categoryDTO);
                        if (deleteCat) {
                            JOptionPane.showMessageDialog(rootPane, "deleted");
                            loadCat();
                            txtCategory.setText(null);
                        }
                    }

                }
            }
        } catch (Exception ex) {
            Logger.getLogger(DialogFormAddItem.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnDeleteMouseClicked

