    private void lblReportsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblReportsMouseClicked
        if (lblReports.isEnabled()) {
            setPanelColour(pnlReports);
            resetPanelColour(pnlGRN, pnlSettings, pnlSupplier, pnlItem, pnlCashier);
            ReportPanel panel = new ReportPanel();
            this.pnlMiddle.removeAll();
            panel.setSize(pnlMiddle.getSize());
            pnlMiddle.add(panel);
            pnlMiddle.revalidate();
            pnlMiddle.repaint();

            this.pnlDashboard.removeAll();
            pnlDashboard.revalidate();
            pnlDashboard.repaint();
        } else {
            DialogFormAdminLogin adminLogin = new lk.ijse.wijithafc.view.DialogFormAdminLogin(this, true);
            adminLogin.setVisible(true);
            if (adminLogin.isLoged()) {
                enableFields("ADMIN");
                setPanelColour(pnlReports);
                resetPanelColour(pnlGRN, pnlSettings, pnlSupplier, pnlItem, pnlCashier);
                ReportPanel panel = new ReportPanel();
                this.pnlMiddle.removeAll();
                panel.setSize(pnlMiddle.getSize());
                pnlMiddle.add(panel);
                pnlMiddle.revalidate();
                pnlMiddle.repaint();
                this.pnlDashboard.removeAll();
                pnlDashboard.revalidate();
                pnlDashboard.repaint();
            }
        }
    }//GEN-LAST:event_lblReportsMouseClicked

