    private void lblItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblItemMouseClicked
        if (lblItem.isEnabled()) {
            setPanelColour(pnlItem);
            resetPanelColour(pnlGRN, pnlSettings, pnlSupplier, pnlReports, pnlCashier);
            ItemPanel itemPanel = new ItemPanel();
            this.pnlMiddle.removeAll();
            itemPanel.setSize(this.pnlMiddle.getSize());
            pnlMiddle.add(itemPanel);
            pnlMiddle.revalidate();
            pnlMiddle.repaint();

            DashBordItem dItem = new DashBordItem();
            this.pnlDashboard.removeAll();
            dItem.setSize(this.pnlDashboard.getSize());
            pnlDashboard.add(dItem);
            pnlDashboard.revalidate();
            pnlDashboard.repaint();
        } else {
            DialogFormAdminLogin adminLogin = new lk.ijse.wijithafc.view.DialogFormAdminLogin(this, true);
            adminLogin.setVisible(true);
            if (adminLogin.isLoged()) {
                enableFields("ADMIN");
                setPanelColour(pnlItem);
                resetPanelColour(pnlGRN, pnlSettings, pnlSupplier, pnlReports, pnlCashier);
                ItemPanel itemPanel = new ItemPanel();
                this.pnlMiddle.removeAll();
                itemPanel.setSize(this.pnlMiddle.getSize());
                pnlMiddle.add(itemPanel);
                pnlMiddle.revalidate();
                pnlMiddle.repaint();

                DashBordItem dItem = new DashBordItem();
                this.pnlDashboard.removeAll();
                dItem.setSize(this.pnlDashboard.getSize());
                pnlDashboard.add(dItem);
                pnlDashboard.revalidate();
                pnlDashboard.repaint();
            }
        }

    }//GEN-LAST:event_lblItemMouseClicked

