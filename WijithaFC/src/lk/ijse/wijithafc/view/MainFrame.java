    private void enableFields(String userType) {
        if (userType.equals("ADMIN")) {
            isAdminLogged = true;
            lblCashier.setEnabled(true);
            lblItem.setEnabled(true);
            lblSupplier.setEnabled(true);
            lblGRN.setEnabled(true);
            lblReports.setEnabled(true);
            lblSettings.setEnabled(true);
            ImageIcon icon = new ImageIcon(this.getClass().getResource("/lk/ijse/wijithafc/images/Admin.png"));
            lblAdmin.setIcon(icon);
            ImageIcon logout = new ImageIcon(this.getClass().getResource("/lk/ijse/wijithafc/images/logout2.png"));
            lblLogOut.setIcon(logout);
        } else {
            lblCashier.setEnabled(true);
            lblItem.setEnabled(false);
            lblSupplier.setEnabled(false);
            lblGRN.setEnabled(false);
            lblReports.setEnabled(false);
            lblSettings.setEnabled(false);
            lblAdmin.setIcon(null);
            ImageIcon logout = new ImageIcon(this.getClass().getResource("/lk/ijse/wijithafc/images/logout1.png"));
            lblLogOut.setIcon(logout);
            lblCashierMouseClicked(null);

        }
    }
