    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        List<GRNDetailDTO> dTOs = new ArrayList<>();
        if (tblGRNDetail.getRowCount() >= 0) {
            for (int i = 0; i < tblGRNDetail.getRowCount(); i++) {
                GRNDetailDTO dTO = new GRNDetailDTO(tblGRNDetail.getValueAt(i, 0).toString(), Double.parseDouble(tblGRNDetail.getValueAt(i, 3).toString()), Integer.parseInt(tblGRNDetail.getValueAt(i, 4).toString()));
                dTOs.add(dTO);
            }
        }
        GRNDTO grndto = new GRNDTO(txtGRNID.getText(), txtDate.getText(), txtSupplierID.getText(), dTOs);
        try {
            boolean addGRN = ManageGRNController.addGRN(grndto);
            if (addGRN) {
                JOptionPane.showMessageDialog(null, "GRN has been added successfully");
                clearFields();
                loadNewID();
                loadAllBatches();

            } else {
                JOptionPane.showMessageDialog(null, "added Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(GRNPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveMouseClicked

