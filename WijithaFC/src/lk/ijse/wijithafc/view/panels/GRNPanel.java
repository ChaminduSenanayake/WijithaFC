    private void txtQty1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtQty1ActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblGRNDetail.getModel();
        if (cmbBatch.getSelectedIndex() != -1) {
            try {
                BatchDTO searchBatch = ManageBatchController.searchBatch(cmbBatch.getSelectedItem().toString());
                ItemDTO searchItem = ManageItemsController.searchItem(searchBatch.getItemCode());
                Object[] rawData = {cmbBatch.getSelectedItem().toString(), searchBatch.getItemCode(), searchItem.getDescription(), searchBatch.getCost(), Integer.parseInt(txtQty1.getText())};
                dtm.addRow(rawData);
            } catch (Exception ex) {
                Logger.getLogger(GRNPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_txtQty1ActionPerformed

