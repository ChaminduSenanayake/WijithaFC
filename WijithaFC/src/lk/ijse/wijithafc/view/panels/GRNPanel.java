    private void loadAllSupplier() {

        cmbCompanyName.removeAllItems();
        try {
            List<SupplierDTO> all = ManageSupplierController.getAll();
            if (all != null) {
                for (SupplierDTO supplier : all) {
                    cmbCompanyName.addItem(supplier.getCompanyName());
                }

            }
        } catch (Exception ex) {
            Logger.getLogger(GRNPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadAllBatches() {
        try {
            List<BatchDTO> all = ManageBatchController.getAll();
            cmbBatch.removeAllItems();
            if (all != null) {
                for (BatchDTO batchDTO : all) {
                    cmbBatch.addItem(batchDTO.getBatchID());

                }
            }
        } catch (Exception ex) {
            Logger.getLogger(GRNPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void loadSystemDate() {
        Date d1 = new Date(); //Current System date
        SimpleDateFormat df = new SimpleDateFormat("YYYY-MM-dd");
        String formatDate = df.format(d1);
        txtDate.setText(formatDate);

    }

    private void loadNewID() {
        try {
            String lastID = IDGenerator.getLastID(IDGenerator.IdType.GRN);
            txtGRNID.setText(lastID);
        } catch (Exception ex) {
            Logger.getLogger(GRNPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void clearFields() {
        DefaultTableModel dtm = (DefaultTableModel) tblGRNDetail.getModel();
        dtm.setRowCount(0);
        txtQty1.setText(null);
    }

    @Override
    public void updateObservers() throws Exception {
        loadAllBatches();
        loadAllSupplier();
        loadNewID();
    }
